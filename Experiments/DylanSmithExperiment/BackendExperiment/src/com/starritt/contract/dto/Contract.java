package com.starritt.contract.dto;

public class Contract {
    private String name;
    private int id;

    public void setName(String setName)
    {
        name = setName;
    }

    public String getName()
    {
        return name;
    }

    public void setId(int setId)
    {
        id = setId;
    }

    public int getId()
    {
        return id;
    }

}
